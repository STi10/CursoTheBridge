# Lista de números
numeros = [4, 6, 8, -1]

# Máximo de la lista
maximo = max(numeros)
print("Máximo de la lista:", maximo)

# Suma de los elementos de la lista
suma = sum(numeros)
print("Suma de los elementos:", suma)

# Redondear el float a 3 decimales
numero_float = 63.451256965
redondeado = round(numero_float, 3)
print("Redondeado a 3 decimales:", redondeado)

# Valor absoluto
valor = -74
absoluto = abs(valor)
print("Valor absoluto de -74:", absoluto)